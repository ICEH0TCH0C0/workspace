SELECT STUDENT_NAME AS "학생 이름", STUDENT_ADDRESS AS "주소지"
FROM TB_STUDENT
ORDER BY STUDENT_NAME;

--2. 
SELECT STUDENT_NAME, STUDENT_SSN
FROM TB_STUDENT
WHERE ABSENCE_YN = 'Y'
ORDER BY MONTHS_BETWEEN(TO_DATE(SUBSTR(SYSDATE, 1, 2), 'RR'), TO_DATE(SUBSTR(STUDENT_SSN, 1, 6), 'RRMMDD'))/12 ASC;

--3.
SELECT STUDENT_NAME, STUDENT_NO, STUDENT_ADDRESS
FROM TB_STUDENT
WHERE STUDENT_ADDRESS IN (SELECT STUDENT_ADDRESS 
                            FROM TB_STUDENT
                            WHERE STUDENT_ADDRESS LIKE ('%경기도%') 
                                OR STUDENT_ADDRESS LIKE ('%강원도%'))
    AND STUDENT_NO NOT LIKE 'A%';

--4. 
SELECT PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '법학과'
ORDER BY MONTHS_BETWEEN(TO_DATE(SUBSTR(SYSDATE, 1, 2), 'RR'), TO_DATE(SUBSTR(PROFESSOR_SSN, 1, 6), 'RRMMDD'))/12 DESC;

--5.
SELECT STUDENT_NO, TO_CHAR(POINT, 9.99) POINT
FROM TB_GRADE
WHERE CLASS_NO = 'C3118100' AND TERM_NO LIKE '200402'
GROUP BY STUDENT_NO, POINT
ORDER BY POINT DESC, STUDENT_NO ASC;

