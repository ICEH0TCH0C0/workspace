    AND TB_CLASS_PROFESSOR.PROFESSOR_NO IS NULL;

--14. ***
SELECT STUDENT_NAME, NVL(PROFESSOR_NAME,'지도교수 미지정')
FROM TB_STUDENT
LEFT JOIN TB_PROFESSOR ON TB_PROFESSOR.PROFESSOR_NO = TB_STUDENT.COACH_PROFESSOR_NO
JOIN TB_DEPARTMENT ON TB_DEPARTMENT.DEPARTMENT_NO = TB_STUDENT.DEPARTMENT_NO
WHERE DEPARTMENT_NAME = '서반아어학과';

--15. 
SELECT STUDENT_NO AS 학번, STUDENT_NAME AS 이름, DEPARTMENT_NAME AS 학과이름, 
    AVG(POINT) AS 평점
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
JOIN TB_GRADE USING(STUDENT_NO)
WHERE ABSENCE_YN = 'N'
GROUP BY STUDENT_NO, STUDENT_NAME, DEPARTMENT_NAME
HAVING  AVG(POINT) >= 4.0;

--16. ***
SELECT TB_CLASS.CLASS_NO, TB_CLASS.CLASS_NAME, AVG(POINT)
FROM TB_CLASS
JOIN TB_DEPARTMENT ON TB_DEPARTMENT.DEPARTMENT_NO = TB_CLASS.DEPARTMENT_NO
JOIN TB_GRADE ON TB_GRADE.CLASS_NO = TB_CLASS.CLASS_NO
WHERE DEPARTMENT_NAME = '환경조경학과' AND CLASS_TYPE LIKE '전공%'
GROUP BY TB_CLASS.CLASS_NO, TB_CLASS.CLASS_NAME;

