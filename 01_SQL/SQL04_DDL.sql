SELECT CLASS_NO, CLASS_NAME, COUNT
FROM (SELECT CLASS_NO, CLASS_NAME, COUNT(*) AS COUNT
        FROM TB_GRADE
        JOIN TB_CLASS USING(CLASS_NO)
        WHERE SUBSTR(TERM_NO, 1, 4) >= (SELECT MAX(SUBSTR(TERM_NO, 1, 4)) - 3
                                            FROM TB_GRADE)
        GROUP BY CLASS_NO, CLASS_NAME
        ORDER BY COUNT(*) DESC)
WHERE ROWNUM <= 3;

SELECT 과목번호, 과목이름, "누적수강생수(명)"
FROM (SELECT CLASS_NO 과목번호, CLASS_NAME 과목이름, COUNT(*) "누적수강생수(명)"
      FROM TB_GRADE
           JOIN TB_CLASS USING(CLASS_NO)
      WHERE TERM_NO LIKE '2009%'
            OR TERM_NO LIKE '2008%'
            OR TERM_NO LIKE '2007%'
            OR TERM_NO LIKE '2006%'
            OR TERM_NO LIKE '2005%'
      GROUP BY CLASS_NO, CLASS_NAME
      ORDER BY 3 DESC)
WHERE ROWNUM <= 3;