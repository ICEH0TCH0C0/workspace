AS (SELECT STUDENT_NO AS 학번, 
            STUDENT_NAME AS 학생이름, 
            STUDENT_ADDRESS AS 주소
        FROM TB_STUDENT);

--11. VIEW를 만들때 SELECT문에서 ORDER BY를 사용할 수 없다. 
CREATE VIEW VW_지도면담
AS (SELECT STUDENT_NAME AS 학생이름, 
            DEPARTMENT_NAME AS 학과이름, 
            PROFESSOR_NAME AS 지도교수이름
        FROM TB_STUDENT
        JOIN TB_DEPARTMENT 
            ON TB_STUDENT.DEPARTMENT_NO = TB_DEPARTMENT.DEPARTMENT_NO
        LEFT JOIN TB_PROFESSOR 
            ON TB_STUDENT.COACH_PROFESSOR_NO = TB_PROFESSOR.PROFESSOR_NO);

--12.
CREATE VIEW VW_학과별학생수
AS (SELECT DEPARTMENT_NAME, COUNT(*) AS STUDENT_COUNT
        FROM TB_STUDENT
        JOIN TB_DEPARTMENT 
            ON TB_STUDENT.DEPARTMENT_NO = TB_DEPARTMENT.DEPARTMENT_NO
        GROUP BY DEPARTMENT_NAME);
        
